#Pedir numeros al usuario
numero1 = int(input("Ingresa un número: "))
numero2 = int(input("Ingresa un número: "))

#Imprimir resultados

#Suma
print(f'La suma es: {numero1 + numero2}')

#Resta
print(f'La suma es: {numero1 - numero2}')

#Multiplicacion
print(f'La suma es: {numero1 * numero2}')

#Division
print(f'La suma es: {numero1 / numero2}')

#Modulo
print(f'La suma es: {numero1 % numero2}')